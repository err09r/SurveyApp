@Composable
fun SurveyAppTheme(
    darkTheme: Boolean = true, //isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
//    val dynamicColor = false //Build.VERSION.SDK_INT >= Build.VERSION_CODES.S
    val colorScheme = surveyAppDarkColorScheme //when {
//        dynamicColor && darkTheme -> dynamicDarkColorScheme(LocalContext.current)
//        dynamicColor && !darkTheme -> dynamicLightColorScheme(LocalContext.current)
//        darkTheme -> surveyAppDarkColorScheme
//        else -> surveyAppLightColorScheme
//    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.surface.toArgb()
            window.navigationBarColor = colorScheme.surface.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = surveyAppTypography,
        shapes = surveyAppShapes,
        content = content
    )
}
